package com.mru.methods;

public class MRU {
	// in static method we need to keep static before written type
		//method without parameter without return type
			public static void dsa1() {
				System.out.println("Welcome ");
			}
			
			//method with parameter without return type
			public static void dsa2(String name) {
				System.out.println(name);
			}
			
			//method with no parameter and with return type
			public static String dsa3() {
				return "Welcome to Leet code";
			}
			
			//method with parameter and with return type
			public static int dsa4(int num) {
				return num;
			}

	}


